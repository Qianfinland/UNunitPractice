
namespace Calculator
{
    public class SimpleCalculator
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Multiply(int a, int b)
        {
            //bug for demo 
            return a * b;
        }

        public double AddDoubles(double a, double b)
        {
            return a + b;
        }
    }
}
